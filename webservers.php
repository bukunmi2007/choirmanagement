<h1 class="text-center">Apache HTTP Server</h1>

<p class="text-justify">This is the most popular web server in the world developed by the Apache Software Foundation.
 Apache web server is an open source software and can be installed on almost all operating systems including Linux, 
 UNIX, Windows, FreeBSD, Mac OS X and more. About 60% of the web server machines run the Apache Web Server.</p>


<h1 class="text-center">Internet Information Services (IIS)</h1>

<p class="text-justify">The Internet Information Server (IIS) is a high performance Web Server from Microsoft.
This web server runs on Windows NT/2000 and 2003 platforms (and may be on upcoming new Windows version also). 
IIS comes bundled with Windows NT/2000 and 2003; Because IIS is tightly integrated with the operating system 
so it is relatively easy to administer it.</p>


<h1 class="text-center">Sun Java System Web Server</h1>

<p class="text-justify">This web server from Sun Microsystems is suited for medium and large web sites. 
Though the server is free it is not open source. It however, runs on Windows, Linux and UNIX platforms. 
The Sun Java System web server supports various languages, scripts and technologies required for Web 2.0 such as 
JSP, Java Servlets, PHP, Perl, Python, and Ruby on Rails, ASP and Coldfusion etc.</p>

