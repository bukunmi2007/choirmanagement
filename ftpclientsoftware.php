<h1 class="text-center">FileZilla</h1>

<p class="text-justify">Topping the list is FileZilla, an open source FTP client. 
It’s fast, being able to handle simultaneous transmissions (multi-threaded transfers), 
and supports SFTP and FTPS (which stands for FTP over SSL). What’s more, it’s available on all operating systems, 
so if you work on multiple computers — like if you’re forced to use Windows at work but you have a Mac at home — you don’t need 
to use a different application for your file-transferring needs. Available on Windows, Mac OS and Linux</p>


<h1 class="text-center">Cyberduck</h1>

<p class="text-justify">Cyberduck can take care of a ton of your file-transferring needs: SFTP, WebDav, Amazon S3, and more. 
It has a minimalist UI, which makes it super easy to use. Available on Windows, and Mac OS</p>

<h1 class="text-center">Classic FTP</h1>

<p class="text-justify">Classic FTP is a file transfer client that’s free for non-commercial use. 
It has a very simple interface, which is a good thing, because it makes it easy and intuitive to use.
I like its “Compare Directories” feature that’s helpful for seeing differences between your local and remote files.</p>



